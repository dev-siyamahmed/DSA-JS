
// ধরা যাক, আমাদের কাছে কিছু সংখ্যার একটি অ্যারে আছে:

let numbers = [10, 20, 30, 40, 50];

// এখানে, numbers একটি অ্যারে যা পাঁচটি সংখ্যা (10, 20, 30, 40, 50) সংরক্ষণ করে রেখেছে।


// আমরা ইন্ডেক্স ব্যবহার করে অ্যারের উপাদানগুলোতে অ্যাক্সেস করতে পারি। যেমন:

console.log(numbers[0]); // আউটপুট: 10
console.log(numbers[3]); // আউটপুট: 40



// যোগ (Insertion): একটি নতুন উপাদান যোগ করা। সাধারণত, অ্যারের শেষে যোগ করা হয়।
numbers.push(60); // এখন numbers = [10, 20, 30, 40, 50, 60]

// অপসারণ (Deletion): একটি উপাদান অপসারণ করা। সাধারণত, অ্যারের শেষের উপাদানটি অপসারণ করা হয়।
numbers.pop(); // এখন numbers = [10, 20, 30, 40, 50]


// আপডেট (Update): অ্যারের একটি নির্দিষ্ট ইন্ডেক্সে উপাদান পরিবর্তন করা।
numbers[1] = 25; // এখন numbers = [10, 25, 30, 40, 50]


// অনুসন্ধান (Search): একটি নির্দিষ্ট উপাদান খুঁজে পাওয়া।
let index = numbers.indexOf(30); // আউটপুট: 2